<h3 style="font-weight:normal;font-size:14px;color:#9a9a9a;margin:0px;">May 2010</h3>
<br>
Alongside the traditional tourism of ancient temples and beautiful scenery in Cambodia, places associated with the Khmer Rouge regime are increasingly making it on to travelers’ checklists. Cambodia recently announced plans to <a style="color: rgb(51, 151, 186);"
href="http://www.usatoday.com/travel/destinations/2010-03-11-cambodia-khmer-rouge-sites_N.htm"target="new"> preserve and develop 14 sites in Anlong Veng</a>, the last stronghold of the Khmer Rouge, which claimed millions of victims between 1975 and 1979. The government believes these sites “will increase understanding of the Khmer Rouge’s last political leadership,” but critics cast it as a <a style="color: rgb(51, 151, 186);"
href="http://www.independent.co.uk/news/world/asia/cambodia-puts-the-cremation-site-of-pol-pot-on-historic-tourist-trail-1919472.html"target="new">
“scheme to profit from the country’s genocidal past.”</a> Around the world, people flock to places remembering painful histories; some describe this as “dark tourism” and criticize such sites for trivializing or exploiting suffering. But Sites of Conscience that remember atrocities resist being simplified as sites of suffering whose visitors are passive voyeurs. Instead, Sites of Conscience intentionally invite all visitors - locals, tourists, students, and the general public - to be active participants in addressing the issues the site raises, past and present.<div style="float: left; margin: 4px 10px 1px 0px; width: 275px;
color: rgb(153, 153, 153); font-size: 11px; line-height: 14px;"><img
src="http://www.sitesofconscience.org/wp-content/uploads/Tuol-Sleng.jpg" style="margin-bottom: 3px;"
border="0" height="195" width="275"><br>Students discuss the history and legacies of the Khmer Rouge at the Tuol Sleng Genocide Museum.</div> <br> In Cambodia, the <a style="color: rgb(51, 151, 186);"
href="http://www.sitesofconscience.org/activities/new-programs/en/#Tuol Sleng" target="new">Tuol Sleng Genocide Museum</a>, a former detention center, is engaging local youth in a program that interrogates the site’s history to catalyze action. Students explore the site, discuss its history and legacies today and decide on actions they would like to take to shape Cambodian society. 
In Chile, survivors from the <a
style="color: rgb(51, 151, 186);"
href="http://www.sitesofconscience.org/sites/villa-grimaldi/en/"
target="new">Villa Grimaldi Peace Park</a>, a former secret torture and detention center from the Pinochet dictatorship, lead interactive tours that connect the site’s history to social concerns today and engage visitors in conversation about their own role in human rights. This kind of “Conscience tourism” strives to re-imagine both the role of Sites and their visitors.
