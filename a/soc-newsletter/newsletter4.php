style="margin-bottom: 3px;" border="0" height="141" width="200"><br>Portraits tell the stories of numerous immigrants that crossed the U.S.-Mexican border in the 1930s and 1940s. Photo: Casasola Studio photographs, PH041. University of Texas at El Paso Library, Special Collections Department</div> In El Paso, Texas, the largest border community in the U.S., the <a style="color: rgb(51, 151, 186);"href="http://academics.utep.edu/Default.aspx?tabid=20021"target="new">University of Texas El Paso (UTEP)</a> is gathering numerous portraits, images and personal stories in a <a style="color: rgb(51, 151, 186);"href="http://www.sitesofconscience.org/activities/en/#UTEP"target="new">new exhibition</a> that tells the tale of the thousands who crossed the border from Mexico during the 1930s and 1940s. Highlighting the different experiences of these immigrants and connecting them to immigration today, UTEP is providing new opportunities for reflection and engaged conversation on the complexities of immigration past and present. 
