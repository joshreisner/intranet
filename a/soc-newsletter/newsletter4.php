<h3 style="font-weight: normal; font-size: 14px; color: rgb(154, 154, 
154); margin: 0px;">July 2010</h3>
<br>
<a style="color: rgb(51, 151, 186);" 
href="http://www.nytimes.com/2010/04/18/opinion/18sun3.html?_r=1"target="new"> “Over the edge”</a> or <a style="color: rgb(51, 151, 186);" 
href="http://www.philly.com/inquirer/world_us/20100429_For_many_in_Arizona__a_law_long_overdue.html" target="new">“overdue”</a>? Arizona’s new <a style="color: rgb(51, 151, 186);"href=" http://edition.cnn.com/2010/POLITICS/04/23/immigration.faq/index.html" target="new"> immigration law</a>, which allows local police to arrest anyone unable to verify their immigration status, has divided Americans. Fearing this law will open the door to rampant discrimination and racial profiling, critics have expressed their outrage with boycotts and protest. But supporters have embraced the law as a positive step towards national security and economic growth. These clashes reflect a long-rooted anxiety about immigration issues in the United States. Meanwhile in Europe,  <a style="color: rgb(51, 
151, 186);" 
href=" http://www.nytimes.com/2009/06/23/world/europe/23france.html?_r=1&scp=1&sq=france%20burqa&st=cse"
 target="new"> France </a>, <a style="color: rgb(51, 151, 186);" 
href=" http://www.cnn.com/2010/WORLD/europe/04/29/belgium.burqa.ban/index.html" 
target="new"> Belgium </a> and <a style="color: rgb(51, 151, 186);" 
href=" http://www.nytimes.com/2010/06/24/world/europe/24iht-spain.html"target="new"> Spain</a> have made headlines with their own immigration-related controversy. Taking steps to ban the wearing of burqas in public, the countries have fueled divisive debates on religious freedom, discrimination and xenophobia. The myriad opinions and concerns rising across America and Europe call for spaces for dialogue where all voices can be heard.
<a style="color: rgb(51, 151, 186);" 
href="http://www.sitesofconscience.org/resources/networks/immigration/en/"target="new"> Immigration Sites of Conscience</a> offer unique opportunities for constructive dialogue by enabling visitors to learn from immigration history to understand today’s debates, and by inviting them to share their own experiences and perspectives. <div style="float: left; margin: 4px 10px 1px 
0px; width: 275px; color: rgb(153, 153, 153); font-size: 11px; 
line-height: 14px;"><img 
src="http://www.sitesofconscience.org/wp-content/uploads/Bois-du-Cazier.jpg" 
style="margin-bottom: 3px;" border="0" height="195" width="275"><br>Le Bois du Cazier, one of the sites to open transatlantic dialogue on immigration and xenophobia.</div> Now, through <a style="color: rgb(51, 151, 186);" 
href="http://www.sitesofconscience.org/activities/en/#MCCA"target="new"><i>Navigating Difference</i></a>, a new interactive installation and program, Sites of Conscience are taking this conversation transnational.  The <a style="color: rgb(51, 151, 186);" 
href="http://www.nps.gov/elis/index.htm"target="new"> Ellis Island Immigration Museum</a> in the U.S., the Italy's <a style="color: rgb(51, 151, 186);" 
href="http://www.museidigenova.it/spip.php?rubrique24"target="new">Mu.MA/Galata Museo del Mare</a> and Belgium’s <a style="color: rgb(51, 151, 186);" 
href=" http://www.leboisducazier.be/"target="new"> Le Bois du Cazier</a>, each tracing different paths that migrants have journeyed through time, will simultaneously invite visitors to answer a common question such as “Does immigration benefit my community?”. Through the installation, visitors will share their opinions and see the responses from within their communities and from the sites in other countries. Beginning with a simple question, this new program will engage visitors in conversations that can be the first step in ‘navigating differences’ on immigration in Europe and the United States.

<p></p>
<p>Around the world, Sites of Conscience are opening new dialogues on today’s critical issues:</p>
<ul style="list-style-type: none; margin: 0px; padding: 0px;">
<li style="background-image: 
