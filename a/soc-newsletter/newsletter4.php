 src="http://www.sitesofconscience.org/wp-content/uploads/District-Six.jpg" style="margin-bottom: 
3px;" border="0" height="120" width="200"><br>District Six Museum's exhibit on football explores a history of racism in the game.</div> With World Cup fever still in the air, <a style="color: rgb(51, 151, 186);"href="http://www.sitesofconscience.org/sites/district-six/en/"target="new">District Six Museum’s</a> (South Africa) new exhibit examines what happens off the field as it explores the history of football in South Africa.  <a style="color: rgb(51, 151, 186);" 
href="http://www.sitesofconscience.org/activities/en/#offside"target="new"><i>Offside: Kick Ignorance Out! Football Unites, Racism Divides</i></a> explores the prejudice many renowned South African football players encountered and encourages visitors to question how racism impacts one of the world’s favorite sports.
</li>
<br>
<li style="background-image: 
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
 background-repeat: no-repeat; padding: 0px 0px 10px 30px; min-height: 
50px;"> 
<a style="color: rgb(51, 151, 186);"href="http://www.slcgg.org/"target="new">Campaign for Good Governance (CGG)</a> (Sierra Leone) is launching <a style="color: rgb(51, 151, 186);"href="http://www.sitesofconscience.org/activities/en/#CGG"target="new">new public dialogues</a> at Sierra Leone’s Special Court where the crimes of the country’s civil war (1991-2002) were prosecuted. Stakeholders, community members and students will explore issues of justice and accountability and how the iconic site could become a space for healing and reconciliation. At the Coalition’s <a style="color: rgb(51, 151, 186);"href="http://www.sitesofconscience.org/activities/en/#workshop"target="new">first West African workshop</a> in Sierra Leone, CGG shared this initiative with sites from Liberia, Senegal, Kenya and Uganda. Together they identified strategies to develop places of memory as centers for dialogue on unresolved questions around justice and impunity in post-conflict democracies.
</li>
<br>
<li style="background-image: 
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
