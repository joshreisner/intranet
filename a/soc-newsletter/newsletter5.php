color: rgb(153, 153, 153); font-size: 11px; line-height: 14px;"><img
src="http://www.sitesofconscience.org/wp-content/uploads/VillaGrimaldi-Chile2-SoC.jpg"
style="margin-bottom: 3px;" border="0" height="141" width="200"><br>Villa Grimaldi Peace Park's (Chile) new program explores the power of testimonies - past and present.</div>Across Latin America, Sites of Conscience from <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbyaaamjyapabmwyaaamsbs/click.php"target="new">Chile</a>, <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewhsaiamjyarabmwyagamsbs/click.php"target="new">El Salvador</a> and <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewhuatamjyatabmwyadamsbs/click.php"target="new">Uruguay</a> are working with the International Coalition&rsquo;s <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewheavamjyapabmwyaiamsbs/click.php"target="new">Project Support Fund</a> to develop groundbreaking new programs. At the <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewhmakamjyatabmwyakamsbs/click.php"target="new">Villa Grimaldi Peace Park</a> in Chile for example, visitors explore the role personal testimonies played in uncovering the site&rsquo;s history and learn how they can gather these &ldquo;living histories&rdquo; themselves. Using diverse tools like exhibitions, publications, workshops and testimonies these new programs aim to open new conversations on all aspects of the past and its legacies today.</li>
