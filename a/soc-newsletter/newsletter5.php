<h3 style="font-weight: normal; font-size: 14px; color: rgb(154, 154,
154); margin: 0px;">September 2010</h3>
<br>
This August, nearly 40 years after the fact, Bangladesh brought before the <a style="color: rgb(51,
151, 186);"
href="http://t.ymlp181.com/ewmsaaamjyanabmwyavamsbs/click.php"
target="new">International Crimes Tribunal</a> key suspects in the genocide of more then three million Bengalis during the 1971 war for independence. At the same time, the <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewmyaaamjyanabmwyaaamsbs/click.php"
target="new"> Extraordinary Chambers in the Courts of Cambodia (ECCC) </a> convicted Kaing Guek Eav (&ldquo;Duch&rdquo;) a prominent figure in the Khmer Rouge regime of the late seventies, for crimes against humanity, a verdict he is now <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjsadamjyapabmwyafamsbs/click.php"target="new"> appealing</a>. As Bangladesh and Cambodia take these momentous legal actions, Sites of Conscience in both countries serve as key spaces for communities to confront the legacies of their past and participate in quests for justice, truth and healing.
<br><br><div style="float: left; margin: 4px 10px 1px
0px; width: 300px; color: rgb(153, 153, 153); font-size: 11px;
line-height: 14px;"><img
src="http://www.sitesofconscience.org/wp-content/uploads/Liberation-War-Museum.jpg"
style="margin-bottom: 3px;" border="0" height="170" width="300"><br>Students await entrance to the Liberation War Museum&rsquo;s mobile museum, which brings the history of the Liberation War to youth across Bangladesh.</div>
In Bangladesh, the <a style="color: rgb(51,
151, 186);"
href="http://t.ymlp181.com/ewjualamjyadabmwyakamsbs/click.php"target="new">Liberation War Museum</a> has been a front-runner in the pursuit of justice, <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjearamjyakabmwyazamsbs/click.php"target="new">&ldquo;projecting the true history of the war&rdquo;</a>: over the last 15 years, the Museum has gathered key <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjmacamjyaxabmwyakamsbs/click.php"target="new">forensic evidence</a>
for the trials and <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjjakamjyavabmwyadamsbs/click.php"target="new">rallied widespread public engagement</a> in the process, including involving thousands of students who were born after the war, through its <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjbadamjyaoabmwyaoamsbs/click.php"target="new">Mobile Museum</a> and educational programs.<br><br> In Cambodia, the <a style="color: rgb(51,
151, 186);"
href="http://t.ymlp181.com/ewjhavamjyavabmwyaramsbs/click.php"target="new">Tuol Sleng Genocide Museum</a>, the site of the former S-21 prison where Duch and others oversaw the torture and killing of thousands, has embraced its role as a Site of Conscience <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjwatamjyadabmwyaaamsbs/click.php"target="new">opening its space to young people</a> to learn about the site&rsquo;s history, talk openly about the ongoing trials and envision a rights-based future.
Against the backdrop of these events, this July, Asian Sites of Conscience from nine countries, including China, India, Nepal and South Korea <a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewjqagamjyafabmwyalamsbs/click.php"target="new">came together</a> in Dhaka, Bangladesh to share and develop methodologies to activate places of memory as centers for engaging the public in open dialogue connecting past and present.
<p></p>
<p>
Around the world, Sites of Conscience continue to make new contributions to remembrance, tolerance and human rights: </p>
<ul style="list-style-type: none; margin: 0px; padding: 0px;">
<li style="background-image:
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
background-repeat: no-repeat; padding: 0px 0px 10px 30px; min-height:
50px;">
<div style="float: right; margin: 4px 0px 1px 10px; width:
200px; color: rgb(153, 153, 153); font-size: 11px; line-height: 14px;"><img
src="http://www.sitesofconscience.org/wp-content/uploads/Nyayo-House.jpg" style="margin-bottom:
3px;" border="0" height="120" width="200"><br>The Torture Chamber of Nairobi&rsquo;s Nyayo House holds vivid evidence of Kenya's violent past.</div> This July, as Kenyans peacefully ratified a <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewjyatamjyaaabmwyalamsbs/click.php"target="new"> new constitution</a>, the International Coalition, the <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbsaiamjyatabmwyaxamsbs/click.php"target="new">Kenyan Human Rights Commission</a> and <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbuaoamjyatabmwyaoamsbs/click.php"target="new">PeaceNet</a> (Kenya) explored how memorialization can help people come to terms with past conflicts, like the post-election violence that rocked the country in 2007.
In a
<a style="color: rgb(51, 151, 186);"
href="http://t.ymlp181.com/ewbeavamjyavabmwyaramsbs/click.php"target="new">memory workshop</a> in Nairobi, 22 civil society organizations stressed the need to preserve and activate places of memory such as the Nyayo House as crucial spaces for people to learn about the past and participate in building a violence-free future.
</li>
<br>
<li style="background-image:
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
background-repeat: no-repeat; padding: 0px 0px 10px 30px; min-height:
50px;">As <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbmatamjyaaabmwyacamsbs/click.php"target="new">Canada&rsquo;s Truth and Reconciliation Commission</a> publicly confronts its Indian Residential Schools history, Americans remain largely unaware of similar efforts to assimilate Native children in the U.S. The International Coalition's <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbjapamjyaxabmwyazamsbs/click.php"target="new">Indian Boarding and Residential Schools Project</a> and <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbbakamjyarabmwyapamsbs/click.php"target="new">Legacy of Hope</a> (Canada) along with former residential school sites are <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbhalamjyanabmwyakamsbs/click.php"target="new"> designing a traveling exhibit </a> that sheds light on boarding school experiences on both sides of the border and invites people to share their own stories and views on this little-known history. </li>
<br>
<li style="background-image:
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
background-repeat: no-repeat; padding: 0px 0px 10px 30px; min-height:
50px;">This September, the Historical Museum of the City of Krakow&rsquo;s (Poland) annual <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbwacamjyadabmwyazamsbs/click.php"target="new">Gestapo Victims Remembrance Day</a>
will invite tourists and locals, young and old, to discover the history of Krakow during the Nazi occupation. A member of the <a style="color: rgb(51, 151, 186);"href="http://t.ymlp181.com/ewbqatamjyaiabmwyacamsbs/click.php"target="new">European Sites of Conscience Network</a>, the museum will use reenactments, exhibitions, movies and discussions to promote awareness of the city&rsquo;s history of struggle and the questions it raises about issues of intolerance and racism today.</li>
<br>
<li style="background-image:
url(&quot;http://members.sitesofconscience.org/a/soc-newsletter/bullet.png&quot;);
