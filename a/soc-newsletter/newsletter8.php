<h3 style="font-weight: normal; font-size: 14px; color: rgb(154, 154,
154); margin: 0px;">Julio 2010</h3>
<br>
<a style="color: rgb(51, 151, 186);"
href="http://www.nytimes.com/2010/04/18/opinion/18sun3.html?_r=1"target="new"> &ldquo;Excesiva&rdquo;</a> o <a style="color: rgb(51, 151, 186);"
href="http://www.philly.com/inquirer/world_us/20100429_For_many_in_Arizona__a_law_long_overdue.html" target="new">&ldquo;no suficiente&rdquo;</a>? La nueva <a style="color: rgb(51, 151, 186);"href="http://edition.cnn.com/2010/POLITICS/04/23/immigration.faq/index.html" target="new">ley de inmigraci&oacute;n</a> de Arizona, que permite a la polic&iacute;a local detener a personas que no puedan comprobar su estatus migratorio, ha dividido a los estadounidenses. Por temor a que esta ley abra la puerta a la discriminaci&oacute;n generalizada y a la segregaci&oacute;n racial, los cr&iacute;ticos han expresado su indignaci&oacute;n con el boicot y la protesta. Pero los partidarios han adoptado la ley como un paso positivo hacia la seguridad nacional y el crecimiento econ&oacute;mico. Estos enfrentamientos reflejan una ansiedad arraigada por largo tiempo sobre temas de inmigraci&oacute;n en los Estados Unidos. Mientras tanto, en Europa, <a style="color: rgb(51,
151, 186);"
href="http://www.nytimes.com/2009/06/23/world/europe/23france.html?_r=1&scp=1&sq=france%20burqa&st=cse"
target="new"> Francia</a>, <a style="color: rgb(51, 151, 186);"
href="http://www.cnn.com/2010/WORLD/europe/04/29/belgium.burqa.ban/index.html"
target="new"> B&eacute;lgica </a> y <a style="color: rgb(51, 151, 186);"
href="http://www.nytimes.com/2010/06/24/world/europe/24iht-spain.html"target="new"> Espa&ntilde;a </a> han mostrado titulares con su propia controversia relacionada con la inmigraci&oacute;n.
Con la adopci&oacute;n de medidas para prohibir el uso de burkas en p&uacute;blico, los pa&iacute;ses han impulsado debates divisivos sobre la libertad religiosa, la discriminaci&oacute;n y la xenofobia. La multiplicidad de opiniones y las crecientes preocupaciones en Estados Unidos y Europa llaman a espacios de di&aacute;logo donde todas las voces puedan ser escuchadas.
<a style="color: rgb(51, 151, 186);"
href="http://www.sitesofconscience.org/recursos/networks/immigration/es/"target="new">Sitios de Conciencia de Inmigraci&oacute;n</a> ofrecen oportunidades &uacute;nicas para el di&aacute;logo constructivo al permitir a los visitantes aprender de la historia de la inmigraci&oacute;n para entender los debates de hoy invit&aacute;ndolos a compartir sus propias experiencias y puntos de vista. <div style="float: left; margin: 4px 10px 1px
0px; width: 275px; color: rgb(153, 153, 153); font-size: 11px;
line-height: 14px;"><img
src="http://www.sitesofconscience.org/wp-content/uploads/Bois-du-Cazier.jpg"
style="margin-bottom: 3px;" border="0" height="195" width="275"><br>
Le Bois du Cazier, uno de los sitios para abrir el di&aacute;logo transatl&aacute;ntico en materia de inmigraci&oacute;n y la xenofobia.</div> Ahora, a trav&eacute;s de <a style="color: rgb(51, 151, 186);"
href="http://www.sitesofconscience.org/activities/en/#MCCA"target="new"><i>
Navegando la Diferencia</i></a>, una nueva instalaci&oacute;n y programa interactivo, los Sitios de Conciencia est&aacute;n llevando esta conversaci&oacute;n a niveles transnacionales.El <a style="color: rgb(51, 151, 186);"
href="http://www.nps.gov/elis/index.htm"target="new">Ellis Island Immigration Museum</a> en los Estados Unidos, <a style="color: rgb(51, 151, 186);"
href="http://www.museidigenova.it/spip.php?rubrique24"target="new"> el Mu.MA/Galata Museo del Mare </a>
en Italia y el museo <a style="color: rgb(51, 151, 186);"
href="http://www.leboisducazier.be/"target="new"> Le Bois du Cazier</a> en B&eacute;lgica, que retratan los diferentes caminos que los inmigrantes han recorrido en el tiempo, har&aacute;n una invitaci&oacute;n simult&aacute;nea a los visitantes a responder una pregunta com&uacute;n como "&iquest;la inmigraci&oacute;n beneficia a mi comunidad?" A trav&eacute;s de la instalaci&oacute;n, los visitantes podr&aacute;n compartir sus opiniones y ver las respuestas dentro de sus comunidades y de los sitios en otros pa&iacute;ses. Empezando con una simple pregunta, este nuevo programa involucrar&aacute; a los visitantes en conversaciones que pueden ser el primer paso para "navegar las diferencias" en materia de inmigraci&oacute;n en Europa y los Estados Unidos.
